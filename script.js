function add (a,b) {
    return a + b;
}

function substract (a,b) {
    return a - b;
}

function multiply (a,b) {
    return a * b;
}

function divide (a,b) {
    return a / b;
}

console.log ("3 + 2 = " + add(3,2));
console.log ("3 - 2 = " + substract(3,2));
console.log ("3 * 2 = " + multiply(3,2));
console.log ("5 / 2 = " + divide(5,2));
console.log ("5 + 0 = " + divide(3,0));